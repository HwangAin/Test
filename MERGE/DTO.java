public class DTO {
	private int salary;
	private int days;
	
	public DTO(int salary, int days) {
		this.salary = salary;
		this.days = days;
	}


	public int getSalary() {
		return salary;
	}


	public void setSalary(int salary) {
		this.salary = salary;
	}


	public int getDays() {
		return days;
	}


	public void setDays(int days) {
		this.days = days;
	}
	
	
	
}
